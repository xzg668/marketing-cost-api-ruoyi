-- =============================================================================
-- V159: 报价接入顶级菜单改名为报价需求
-- -----------------------------------------------------------------------------
-- 仅调整展示名称，保留 menu_id/path/perms，避免角色授权和前端路由断裂。
-- =============================================================================

SET NAMES utf8mb4;

UPDATE sys_menu
   SET menu_name = '报价需求',
       update_time = NOW(),
       remark = CASE
         WHEN remark IS NULL OR remark = '' THEN 'V159：顶级菜单由数据接入改名为报价需求'
         WHEN remark LIKE '%V159：顶级菜单由数据接入改名为报价需求%' THEN remark
         ELSE CONCAT(remark, '；V159：顶级菜单由数据接入改名为报价需求')
       END
 WHERE menu_id = 200;
