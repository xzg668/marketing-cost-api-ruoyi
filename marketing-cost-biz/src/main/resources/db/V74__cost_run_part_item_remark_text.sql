-- =============================================================================
-- V74: 放宽部品试算结果备注长度
-- -----------------------------------------------------------------------------
-- 背景：CMS 废料映射接入后，自制件取价会在 lp_cost_run_part_item.remark 写入
--      废料映射/当前价/抵扣额等核对信息。原字段 VARCHAR(200) 容易因多废料或缺价
--      说明触发 MySQL Data truncation，导致整次产品成本计算失败。
--
-- 改动：remark 从 VARCHAR(200) 放宽为 TEXT。Java 层仍会生成短备注，数据库放宽
--      作为防线，避免诊断信息中断试算。
-- =============================================================================

ALTER TABLE lp_cost_run_part_item
  MODIFY COLUMN remark TEXT NULL COMMENT '备注/取价说明';
